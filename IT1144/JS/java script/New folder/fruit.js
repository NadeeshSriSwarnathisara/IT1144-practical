let fruit = "apple";

switch (fruit) {
  case "apple":
    console.log("Apples are great for making cider and pies!");
    break;
  case "banana":
    console.log("Bananas are packed with potassium!");
    break;
  case "orange":
    console.log("Oranges are an excellent source of Vitamin C.");
    break;
  case "mango":
    console.log("Mango is known as the king of fruits!");
    break;
  default:
    console.log("That sounds like a delicious fruit, but I don't know much about it.");
}
